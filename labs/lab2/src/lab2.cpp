//Записывает число n в файл как строку
void write_int(IFile& file, int n) {
    if (n < 0) //если отриц, то записываем знак минус и делаем число положительным
    {
        file.write("-", 1);
        n = -n;
    }
    
    char digits[11];// массив для хранения цифр в обратном порядке, 11 тк int 10 цифр + на знак
    int count = 0;//кол-во полученных цифр
    
    if (n == 0) {
        file.write("0", 1);
    } 
    else {
        while (n > 0) {
            digits[count++] = '0' + (n % 10);
            // символ '0' как код ASCII - это число 48
            //при сумме с остатоком от дел получим код - сивол '1' '2' и тд 
            n /= 10;
        }
        for (int i = count - 1; i >= 0; i--) {
            file.write(&digits[i], 1); //запись цифр в правильном порядке
        }
    }
}


